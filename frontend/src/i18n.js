function normalizeLang(code = '') {
  const value = String(code || '').toLowerCase()
  if (value.startsWith('ru')) return 'ru'
  if (value.startsWith('en')) return 'en'
  if (value.startsWith('kk') || value.startsWith('kz')) return 'kk'
  if (value.startsWith('uz')) return 'uz'
  if (value.startsWith('tr')) return 'tr'
  if (value.startsWith('es')) return 'es'
  return FALLBACK_LANG
}

function getInitialLang() {
  if (typeof window === 'undefined') return FALLBACK_LANG
  const saved = window.localStorage?.getItem('lang')
  if (saved) return normalizeLang(saved)
  const tgLang = window.Telegram?.WebApp?.initDataUnsafe?.user?.language_code
  if (tgLang) return normalizeLang(tgLang)
  return normalizeLang(window.navigator?.language || FALLBACK_LANG)
}

function interpolate(text, params) {
  if (!params) return text
  return text.replace(/\{(\w+)\}/g, (match, key) => (
    params[key] === undefined ? match : String(params[key])
  ))
}

const I18nContext = createContext({
  lang: FALLBACK_LANG,
  setLang: () => {},
  t: (key) => key,
  languages: LANGUAGES
})

export function I18nProvider({ children }) {
  const [lang, setLangState] = useState(getInitialLang)

  function setLang(nextLang) {
    const normalized = normalizeLang(nextLang)
    setLangState(normalized)
    if (typeof window !== 'undefined') {
      window.localStorage?.setItem('lang', normalized)
    }
  }

  const value = useMemo(() => ({
    lang,
    setLang,
    languages: LANGUAGES,
    t: (key, params) => {
      const base = translations[lang]?.[key] || translations[FALLBACK_LANG]?.[key] || key
      return interpolate(base, params)
    }
  }), [lang])

  return createElement(I18nContext.Provider, { value }, children)
}

export function useI18n() {
  return useContext(I18nContext)
}
